public class For{
    public static void main(String args[]){
        for(int i=1;i<=10;i++){
            System.out.println(i);
        }
        System.out.println("using while loop");
        int j=1;
        while(j<=10){
            System.out.println(j);
            j++;
        }
        int k=1;

        do{
            System.out.println(k);
            k++;
        }while(k<=12);
    }
}